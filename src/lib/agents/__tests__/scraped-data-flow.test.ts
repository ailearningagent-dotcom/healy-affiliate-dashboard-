// TEST 4: Lead Validator — Handles enriched data correctly
// ========================================================================

describe("Lead validator with enriched scraped data", () => {
  it("passes fully enriched leads with email and phone", () => {
    const lead = createScrapedLead();
    const { validLeads, summary } = validateAndFilterLeads([lead]);
    expect(validLeads).toHaveLength(1);
    expect(summary.skippedLeads).toBe(0);
    expect(summary.validLeads).toBe(1);
  });

  it("passes leads with only email (no phone)", () => {
    const lead = createScrapedLead({ phone: "" });
    const { validLeads } = validateAndFilterLeads([lead]);
    expect(validLeads).toHaveLength(1);
  });

  it("passes leads with only phone (no email)", () => {
    const lead = createScrapedLead({ email: "" });
    const { validLeads } = validateAndFilterLeads([lead]);
    expect(validLeads).toHaveLength(1);
  });

  it("skips leads with both email and phone blank", () => {
    const lead = createScrapedLead({ email: "", phone: "" });
    const { validLeads, summary } = validateAndFilterLeads([lead]);
    expect(validLeads).toHaveLength(0);
    expect(summary.skippedLeads).toBe(1);
    expect(summary.skippedReasons["No email or phone — unreachable"]).toBe(1);
  });

  it("skips leads with invalid email format", () => {
    const lead = createScrapedLead({ email: "not-an-email" });
    const { validLeads } = validateAndFilterLeads([lead]);
    expect(validLeads).toHaveLength(0);
  });

  it("allows info@, hello@, contact@, support@ emails (legitimate business emails)", () => {
    for (const email of [
      "info@balancehealth.com",
      "hello@wellness.com",
      "contact@clinic.com",
      "support@studio.com",
    ]) {
      const lead = createScrapedLead({ email });
      const { validLeads } = validateAndFilterLeads([lead]);
      expect(validLeads).toHaveLength(1);
    }
  });

  it("skips noreply@ and example@ emails (non-deliverable)", () => {
    for (const email of ["noreply@company.com", "no-reply@test.com", "example@domain.com"]) {
      const lead = createScrapedLead({ email });
      const { validLeads } = validateAndFilterLeads([lead]);
      expect(validLeads).toHaveLength(0);
    }
  });

  it("skips leads with empty/invalid business name", () => {
    const lead = createScrapedLead({ name: "" });
    const { validLeads } = validateAndFilterLeads([lead]);
    expect(validLeads).toHaveLength(0);
  });

  it("validates a mixed batch and returns correct counts", () => {
    const leads = [
      createScrapedLead({ name: "Good Lead 1", email: "info@good1.com" }),
      createScrapedLead({ name: "Good Lead 2", email: "hello@good2.com", phone: "" }),
      createScrapedLead({ name: "", email: "info@bad.com" }),             // no name
      createScrapedLead({ email: "", phone: "" }),                        // no contact
      createScrapedLead({ email: "noreply@bad.com" }),                    // non-deliverable
    ];
    const { validLeads, skippedLeads, summary } = validateAndFilterLeads(leads);
    expect(validLeads).toHaveLength(2);
    expect(skippedLeads).toHaveLength(3);
    expect(summary.validLeads).toBe(2);
    expect(summary.skippedLeads).toBe(3);
    expect(summary.totalLeads).toBe(5);
  });
});

