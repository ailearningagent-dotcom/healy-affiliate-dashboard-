// TEST 2: Email Personalizer — Inject scraped data into templates
// ========================================================================

describe("Email personalizer with enriched scraped data", () => {
  it("injects {company} placeholder from lead company name", () => {
    const lead = createLead();
    const result = injectLeadData("I came across {company} and was impressed!", lead);
    expect(result).toBe("I came across Harmony Wellness Center and was impressed!");
  });

  it("injects {category} placeholder from parsed notes", () => {
    const lead = createLead({ notes: MOCK_NOTES_FULL });
    const result = injectLeadData("As a fellow {category}, I wanted to reach out.", lead);
    expect(result).toBe("As a fellow Wellness Center, I wanted to reach out.");
  });

  it("injects {rating} and {reviews} placeholders from parsed notes", () => {
    const lead = createLead({ notes: MOCK_NOTES_FULL });
    const result = injectLeadData("Your {rating}★ rating with {reviews} reviews is impressive!", lead);
    expect(result).toBe("Your 4.5★ rating with 127 reviews is impressive!");
  });

  it("injects {address} placeholder from parsed notes", () => {
    const lead = createLead({ notes: MOCK_NOTES_FULL });
    const result = injectLeadData("Located at {address}.", lead);
    expect(result).toBe("Located at 123 Main Street, Portland, OR.");
  });

  it("injects {website} placeholder from sourceUrl", () => {
    const lead = createLead({ sourceUrl: "https://harmonywellness.com" });
    const result = injectLeadData("Visit {website}.", lead);
    expect(result).toBe("Visit https://harmonywellness.com.");
  });

  it("injects all placeholders in a realistic template and removes all template braces", () => {
    const lead = createLead({ notes: MOCK_NOTES_FULL });
    const template =
      "Hi there,\n\nI came across {company} — a {category} — and was impressed by your {rating}★ rating across {reviews} reviews.\n\nLocated at {address}, you're in a great area for what we do.";
    const result = injectLeadData(template, lead);
    expect(result).toContain("Harmony Wellness Center");
    expect(result).toContain("Wellness Center");
    expect(result).toContain("4.5");
    expect(result).toContain("127");
    expect(result).toContain("123 Main Street, Portland, OR");
    expect(result).not.toContain("{company}");
    expect(result).not.toContain("{category}");
    expect(result).not.toContain("{rating}");
    expect(result).not.toContain("{reviews}");
    expect(result).not.toContain("{address}");
  });

  it("gracefully removes missing placeholders when no scraped data in notes", () => {
    const lead = createLead({ notes: MOCK_NOTES_EMPTY });
    const template =
      "Hi {company}, your {category} ({rating}★, {reviews} reviews) at {address} looks great!";
    const result = injectLeadData(template, lead);
    // Company should still be present (from lead.company field)
    expect(result).toContain("Harmony Wellness Center");
    // Missing parsed fields should be cleaned up
    expect(result).not.toContain("{company}");
    expect(result).not.toContain("{category}");
    expect(result).not.toContain("{rating}");
    expect(result).not.toContain("{reviews}");
    expect(result).not.toContain("{address}");
    // No double spaces or trailing commas from removed placeholders
    expect(result).not.toContain("  ");
  });

  it("handles empty notes gracefully", () => {
    const lead = createLead({ notes: "" });
    const result = injectLeadData("Hello {company}!", lead);
    expect(result).toBe("Hello Harmony Wellness Center!");
  });

  it("handles {businessDetails} placeholder with full notes string", () => {
    const lead = createLead({ notes: MOCK_NOTES_FULL });
    const result = injectLeadData("Details: {businessDetails}", lead);
    expect(result).toContain("4.5★");
    expect(result).toContain("127 reviews");
    expect(result).toContain("Wellness Center");
    expect(result).not.toContain("{businessDetails}");
  });

  it("handles {score} placeholder", () => {
    const lead = createLead({ score: 82 });
    const result = injectLeadData("Lead score: {score}", lead);
    expect(result).toBe("Lead score: 82");
  });

  it("injects a real Healy Email 1 template correctly", () => {
    const lead = createLead({ notes: MOCK_NOTES_FULL });
    // This is a simplified version of the real wellness-seeker Email 1
    const healyTemplate = `Hi {name},

I hope this email finds you well! My name is Sarah from Healy, and I came across {company} while browsing wellness-focused businesses. I was impressed by what you're building.

As a {category}, I thought you might be interested in frequency technology.

Would you be open to a brief conversation?`;

    const result = injectLeadData(healyTemplate, lead);
    // Company name injected
    expect(result).toContain("Harmony Wellness Center");
    // Category injected
    expect(result).toContain("Wellness Center");
    // No raw placeholders
    expect(result).not.toContain("{company}");
    expect(result).not.toContain("{category}");
    // {name} is intentionally not replaced (done separately by nurture engine)
    expect(result).toContain("{name}");
  });
});

