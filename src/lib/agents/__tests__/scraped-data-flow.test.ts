// TEST 1: Notes Parsing — Extract fields from scraper's notes format
// ========================================================================

describe("Notes parsing helpers (from scraper output)", () => {
  it("extracts rating from full notes", () => {
    expect(extractRatingFromNotes(MOCK_NOTES_FULL)).toBe("4.5");
    expect(extractRatingFromNotes("")).toBe("");
    expect(extractRatingFromNotes("No rating here")).toBe("");
  });

  it("extracts reviews count from notes", () => {
    expect(extractReviewsFromNotes(MOCK_NOTES_FULL)).toBe("127");
    expect(extractReviewsFromNotes("4.5★")).toBe("");
    expect(extractReviewsFromNotes("3.9★ (42 reviews)")).toBe("42");
    // Multiple-word review count
    expect(extractReviewsFromNotes("4.5★ (1,234 reviews)")).toBe("1234");
  });

  it("extracts address from notes", () => {
    expect(extractAddressFromNotes(MOCK_NOTES_FULL)).toBe("123 Main Street, Portland, OR");
    expect(extractAddressFromNotes(MOCK_NOTES_MINIMAL)).toBe("");
    expect(extractAddressFromNotes("No address")).toBe("");
  });

  it("extracts category from notes", () => {
    expect(extractCategoryFromNotes(MOCK_NOTES_FULL)).toBe("Wellness Center");
    expect(extractCategoryFromNotes(MOCK_NOTES_MINIMAL)).toBe("Yoga Studio");
    expect(extractCategoryFromNotes("No category")).toBe("");
  });

  it("extracts fields from minimal notes", () => {
    expect(extractRatingFromNotes(MOCK_NOTES_MINIMAL)).toBe("3.8");
    expect(extractReviewsFromNotes(MOCK_NOTES_MINIMAL)).toBe("42");
    expect(extractCategoryFromNotes(MOCK_NOTES_MINIMAL)).toBe("Yoga Studio");
    expect(extractAddressFromNotes(MOCK_NOTES_MINIMAL)).toBe("");
  });

  it("returns empty strings when no scraped data in notes", () => {
    expect(extractRatingFromNotes(MOCK_NOTES_EMPTY)).toBe("");
    expect(extractReviewsFromNotes(MOCK_NOTES_EMPTY)).toBe("");
    expect(extractAddressFromNotes(MOCK_NOTES_EMPTY)).toBe("");
    expect(extractCategoryFromNotes(MOCK_NOTES_EMPTY)).toBe("");
  });

  it("extracts from realistic multi-city scraper format", () => {
    const nyNotes =
      'Google Maps lead — "wellness center new york". | 4.2★ (89 reviews) | Category: Medical Practice | Located at: 456 Oak Avenue, New York, NY | Phone: +1 (212) 555-8901 | Web: https://drsmithwellness.com';
    expect(extractRatingFromNotes(nyNotes)).toBe("4.2");
    expect(extractReviewsFromNotes(nyNotes)).toBe("89");
    expect(extractAddressFromNotes(nyNotes)).toBe("456 Oak Avenue, New York, NY");
    expect(extractCategoryFromNotes(nyNotes)).toBe("Medical Practice");
  });
});

