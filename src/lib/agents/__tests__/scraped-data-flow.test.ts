/**
 * Mock the format of notes generated by google-maps-scraper's buildNotes().
 * The enriched data (rating, reviews, address, category) is stored within
 * the notes string, and extracted by the parsing helpers in email-personalizer.ts.
 */
const MOCK_NOTES_FULL =
  'Google Maps lead — "wellness center portland". | 4.5★ (127 reviews) | Category: Wellness Center | Located at: 123 Main Street, Portland, OR | Phone: +1 (555) 123-4567 | Web: https://harmonywellness.com';

const MOCK_NOTES_MINIMAL =
  'Google Maps lead — "yoga studio". | 3.8★ (42 reviews) | Category: Yoga Studio';

const MOCK_NOTES_EMPTY = 'Google Maps lead — "search query".';

function createScrapedLead(overrides: Partial<ScrapedLead> = {}): ScrapedLead {
  return {
    name: "Harmony Wellness Center",
    company: "Harmony Wellness Center",
    role: "Wellness Center",
    source: "google-maps",
    sourceUrl: "https://harmonywellness.com",
    score: 75,
    personaType: "wellness-seeker",
    email: "info@harmonywellness.com",
    phone: "+1 (555) 123-4567",
    rating: "4.5",
    reviews: "127",
    address: "123 Main Street, Portland, OR",
    category: "Wellness Center",
    businessDetails:
      "4.5★ (127 reviews) | Category: Wellness Center | Located at: 123 Main Street, Portland, OR | Phone: +1 (555) 123-4567 | Website: https://harmonywellness.com",
    notes: MOCK_NOTES_FULL,
    ...overrides,
  };
}

function createLead(overrides: Partial<Lead> = {}): Lead {
  return {
    id: crypto.randomUUID(),
    name: "Harmony Wellness Center",
    company: "Harmony Wellness Center",
    role: "Wellness Center",
    email: "info@harmonywellness.com",
    phone: "+1 (555) 123-4567",
    source: "google-maps",
    sourceUrl: "https://harmonywellness.com",
    status: "new",
    pipelineStage: "sourced",
    score: 75,
    personaType: "wellness-seeker",
    notes: MOCK_NOTES_FULL,
    createdAt: new Date(),
    ...overrides,
  };
}

