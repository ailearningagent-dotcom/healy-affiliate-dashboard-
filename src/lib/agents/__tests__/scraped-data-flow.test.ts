// TEST 5: computeScrapedLeadScore — Uses enriched fields for scoring
// ========================================================================

describe("computeScrapedLeadScore with enriched data", () => {
  it("computes max score (100) for fully enriched leads", () => {
    const lead = createScrapedLead();
    const score = computeScrapedLeadScore(lead);
    // 50 base + 15 email + 10 phone + 13.5 rating(4.5*3) + 10 reviews>50 + 5 website + 3 address + 2 category = 108.5 → capped at 100
    expect(score).toBe(100);
  });

  it("computes correct intermediate score for leads with some data", () => {
    const lead = createScrapedLead({
      email: "test@example.com",
      phone: "",
      rating: "3.5",
      reviews: "20",
      sourceUrl: "",
      address: "",
      category: "",
    });
    const score = computeScrapedLeadScore(lead);
    // 50 base + 15 email + 10.5 rating(3.5*3 → 11) + 0 reviews(20 < 50 threshold) = 76
    expect(score).toBe(76);
  });

  it("computes base score for leads with no enriched fields", () => {
    const lead = createScrapedLead({
      email: "",
      phone: "",
      rating: "",
      reviews: "",
      sourceUrl: "",
      address: "",
      category: "",
    });
    const score = computeScrapedLeadScore(lead);
    expect(score).toBe(50);
  });
});

